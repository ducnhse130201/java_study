package bycomment.qualifier;

public class Student {
    private Integer age;
    private String name;

    public void setAge(Integer age){
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public Integer getAge() {
        return age;
    }
}
